public class CreatingArray {
    public static void main(String args[]){
        //array for storing marks with size 50
        int marks[] = new int[50];

        //array to store three numbers
        int numbers[] = {1, 2, 12};

        //array to store some names of fruits of type string
        String fruits[] = {"Apple", "Orange", "Grapes"};

        System.out.println(marks[0] + fruits[0] + numbers[0]);


    }
}
